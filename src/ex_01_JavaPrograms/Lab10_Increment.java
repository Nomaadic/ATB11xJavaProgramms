package ex_01_JavaPrograms;

public class Lab10_Increment
{
    public static void main(String[] args) {
        int a = 24;
        System.out.println(a);
        int b = ++a;
        System.out.println(b);
        System.out.println(a);

        int c = 67;
        int d = c++; 
        System.out.println(d);
        System.out.println(c);
        System.out.println(d);
    }
}
